# Exercise 1: Creating Tuples
# Creating the tuples
name_tuple = ("John", "Doe")                      # First name and last name
profession_tuple = ("Software Engineer",)         # Current profession
current_address_tuple = ("123 Main St", "New York, NY", "10001")  # Current address
previous_address_tuple = ("456 Elm St", "Boston, MA", "02118")    # Previous address

# Combining all tuples into a single tuple
combined_tuple = name_tuple + profession_tuple + current_address_tuple + previous_address_tuple

# Display the combined tuple
print("Combined Tuple:")
print(combined_tuple)