# Comparing strings
fname_1 = "john"
fname_2 = "john"
fname_3 = "John"
#This will display True if the two strings are equal
#and False if they aren't equal
print(fname_1, "==", fname_2)
print(fname_1 == fname_2)
#This will display True if the two strings are equal
#and False if they aren't equal
print(fname_1, "==", fname_3)
print(fname_1 == fname_3)
#This will display False if the two strings are equal
#and True if they aren't equal
print(fname_1, "!=", fname_3)
print(fname_1 != fname_3)
