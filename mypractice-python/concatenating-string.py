#Concatenating strings
first_name = "Deepa"
last_name = "Ponnusamy"
print(first_name)
print(last_name)
full_name = first_name + last_name
print(full_name)
full_name_upper = first_name.upper() + last_name.upper()
print(full_name_upper)

# Formatting names for a pamphlet
first_name = "Deepa"
last_name = "Ponnusamy"
full_name_upper = last_name.upper() + ", " + first_name.upper()
print (full_name_upper)
full_name_lower = last_name.lower() + ", " + first_name.lower()
print (full_name_lower)

